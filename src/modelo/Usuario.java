package modelo;

public class Usuario {
	
	private int ip;
	private int port;
	
	
	
	public Usuario(int ip, int port) {
		super();
		this.ip = ip;
		this.port = port;
	}
	public int getIp() {
		return ip;
	}
	public void setIp(int ip) {
		this.ip = ip;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	
	
	
	

}
